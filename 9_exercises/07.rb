# What's the major difference between an Array and a Hash?

# An array is a collection of individual elements, while a hash is a collection of key-value pairs
# for referencing by key.

array = [1, 2, 3, 'a', [4, 5, 6], true]
hash = {:eggs => 'breakfast', :salad => 'lunch', :sandwich => 'lunch', :lasagna => 'dinner'}

p array 
p hash